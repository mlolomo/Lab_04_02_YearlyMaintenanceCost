public class Main {
    public static void main(String[] args) {
        double springCost = 250.00;
        double summerCost = 300.00;
        double fallCost = 150.00;
        double winterCost = 200.00;
        double totalYearlyCost = springCost + summerCost + fallCost + winterCost;

        System.out.println("Spring maintenance cost is $" + springCost);
        System.out.println("Summer maintenance cost is $" + summerCost);
        System.out.println("Fall maintenance cost is $" + fallCost);
        System.out.println("Winter maintenance cost is $" + winterCost);
        System.out.println("The total yearly maintenance cost is $" + totalYearlyCost);
    }
}